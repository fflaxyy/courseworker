axes[1, 0].hist(df_noise['depth_noisy'], bins=40, alpha=0.6, label='С шумом')
axes[1, 0].set_title('Распределение depth')
axes[1, 0].legend()
axes[1, 1].scatter(sample_noise['depth'], sample_noise['depth_noisy'], alpha=0.3, s=10)
