rng = np.random.default_rng(42)
df_noise = df.copy()
df_noise['carat_noisy'] = np.clip(df_noise['carat'] + rng.normal(0, 0.015, size=len(df_noise)), 0, None)
df_noise['depth_noisy'] = df_noise['depth'] + rng.normal(0, 0.25, size=len(df_noise))
sample_noise = df_noise.sample(n=5000, random_state=7)
