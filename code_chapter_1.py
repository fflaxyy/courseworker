# -*- coding: utf-8 -*-
from pathlib import Path
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.preprocessing import LabelEncoder
OUT_DIR = Path('chapter1_figures')
OUT_DIR.mkdir(exist_ok=True)
DATA_PATH = Path('diamonds.csv')
if DATA_PATH.exists():
    df = pd.read_csv(DATA_PATH)
else:
    from plotnine.data import diamonds as builtin_diamonds
    df = builtin_diamonds.copy()
df = df.loc[:, ~df.columns.str.contains('^Unnamed', case=False, regex=True)]
for col in ['cut', 'color', 'clarity']:
    if col in df.columns:
        df[col] = df[col].astype('category')
numeric_cols = ['carat', 'depth', 'table', 'price', 'x', 'y', 'z']
cat_cols = ['cut', 'color', 'clarity']
sns.set_theme(style='whitegrid', font_scale=0.95)
plt.rcParams['font.family'] = 'DejaVu Sans'
fig, axes = plt.subplots(3, 3, figsize=(12, 10))
axes = axes.ravel()
for i, col in enumerate(numeric_cols):
    axes[i].hist(df[col], bins=40, alpha=0.8, edgecolor='black')
    axes[i].axvline(df[col].mean(), linestyle='--', linewidth=1.5, label=f'Среднее: {df[col].mean():.2f}')
    axes[i].axvline(df[col].median(), linestyle=':', linewidth=1.5, label=f'Медиана: {df[col].median():.2f}')
    axes[i].set_title(col)
    axes[i].set_xlabel('Значение')
    axes[i].set_ylabel('Частота')
    axes[i].legend(fontsize=8)
for j in range(len(numeric_cols), len(axes)):
    axes[j].axis('off')
fig.suptitle('Распределение числовых признаков датасета Diamonds Prices', fontsize=14)
fig.tight_layout(rect=[0, 0, 1, 0.97])
fig.savefig(OUT_DIR / 'fig01_numeric_histograms.png', dpi=220, bbox_inches='tight')
plt.close(fig)
sample = df.sample(n=5000, random_state=42)
fig, axes = plt.subplots(1, 3, figsize=(16, 5))
sns.scatterplot(data=sample, x='carat', y='price', hue='cut', alpha=0.45, s=16, ax=axes[0])
axes[0].set_title('Цена от массы (carat) по качеству огранки')
axes[0].set_xlabel('carat')
axes[0].set_ylabel('price, USD')
axes[0].legend(fontsize=7, title='cut')
sns.scatterplot(data=sample, x='x', y='price', hue='clarity', alpha=0.45, s=16, ax=axes[1])
axes[1].set_title('Цена от длины x по чистоте')
axes[1].set_xlabel('x, мм')
axes[1].set_ylabel('price, USD')
axes[1].legend(fontsize=7, title='clarity')
sns.boxplot(data=df, x='cut', y='price', showfliers=False, ax=axes[2])
axes[2].set_title('Распределение цены по качеству огранки')
axes[2].set_xlabel('cut')
axes[2].set_ylabel('price, USD')
axes[2].tick_params(axis='x', rotation=20)
fig.tight_layout()
fig.savefig(OUT_DIR / 'fig02_seaborn_relationships.png', dpi=220, bbox_inches='tight')
plt.close(fig)
df_py = df.copy()
df_py['carat_bin'] = pd.cut(df_py['carat'], bins=np.arange(0.2, 5.31, 0.25), include_lowest=True)
mean_price_by_carat = df_py.groupby('carat_bin', observed=False)['price'].mean().reset_index()
mean_price_by_carat['carat_mid'] = mean_price_by_carat['carat_bin'].apply(lambda x: x.mid)
median_depth_by_cut = df_py.groupby('cut', observed=False)['depth'].median().reindex(['Fair', 'Good', 'Very Good', 'Premium', 'Ideal'])
fig, axes = plt.subplots(1, 2, figsize=(13, 5))
axes[0].plot(mean_price_by_carat['carat_mid'], mean_price_by_carat['price'], marker='o')
axes[0].set_title('Средняя цена по интервалам carat')
axes[0].set_xlabel('Средина интервала carat')
axes[0].set_ylabel('Средняя цена, USD')
axes[0].grid(True, alpha=0.3)
axes[1].bar(median_depth_by_cut.index.astype(str), median_depth_by_cut.values)
axes[1].set_title('Медианная глубина depth по категориям cut')
axes[1].set_xlabel('cut')
axes[1].set_ylabel('Медиана depth, %')
axes[1].tick_params(axis='x', rotation=20)
fig.tight_layout()
fig.savefig(OUT_DIR / 'fig03_pyplot_features.png', dpi=220, bbox_inches='tight')
plt.close(fig)
