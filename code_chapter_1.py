outlier_counts = {}
for col in numeric_cols:
    q1 = df[col].quantile(0.25)
    q3 = df[col].quantile(0.75)
    iqr = q3 - q1
    lower = q1 - 1.5 * iqr
    upper = q3 + 1.5 * iqr
    outlier_counts[col] = int(((df[col] < lower) | (df[col] > upper)).sum())
fig, axes = plt.subplots(1, 2, figsize=(14, 5))
sns.boxplot(data=df[numeric_cols], orient='h', ax=axes[0], showfliers=True)
axes[0].set_title('Диаграммы размаха числовых признаков')
axes[0].set_xlabel('Значение')
axes[1].bar(list(outlier_counts.keys()), list(outlier_counts.values()))
axes[1].set_title('Количество выбросов по IQR')
axes[1].set_ylabel('Количество')
axes[1].tick_params(axis='x', rotation=30)
for i, v in enumerate(outlier_counts.values()):
    axes[1].text(i, v, str(v), ha='center', va='bottom', fontsize=8)
fig.tight_layout()
fig.savefig(OUT_DIR / 'fig06_outliers_boxplots.png', dpi=220, bbox_inches='tight')
plt.close(fig)
